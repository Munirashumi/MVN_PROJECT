package learningjava;

public class CheckNumberIsEvenOrOdd {

	public static void main(String[] args) {
     int a = 39;
     int b = 20;
     
     if(a%2 ==0) {
    	 System.out.println("a is an even number:" + a);
     }else {
    	 System.out.println("a is not a even number");
     }
     if (b%2 ==0) {
    	 System.out.println("b is an even number:"+ b);
     }else {
    	 System.out.println("b is an even number");
    
    
     }
	}

}
